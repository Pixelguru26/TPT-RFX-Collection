
local img = {}

local loaded = false
local clock = 0

-- ==========================================
-- Texture functions
-- ==========================================

local function toIndex(arr, x, y)
	x = math.floor(x)
	y = math.floor(y)
	if x < 0 or y < 0 or x >= arr.w or y >= arr.h then return nil end
	return (y*arr.w + x)*3+1
end
local function getPixel(arr, x, y)
	local i = toIndex(arr, x, y)
	if not i then
		tpt.log("Position", x, y, "invalid")
		return nil
	end
	return arr[i], arr[i+1], arr[i+2]
end
local function setPixel(arr, x, y, r, g, b)
	local i = toIndex(arr, x, y)
	if not i then
		tpt.log("Position", x, y, "invalid")
		return nil
	end
	arr[i] = r
	arr[i + 1] = g
	arr[i + 2] = b
end

local function loadImage(data)
	-- Local variable declaration
	local char = string.char
	local i = 0
	local byte
	local bytearray = {}

	-- Translate data into readable form
	-- (converts each pair of hex digits to an ASCII byte and removes whitespace)
	local dataStr = string.gsub(data, "(%x%x)%s*", function(s)
		byte = tonumber(s, 16)
		i = i + 1
		bytearray[i] = byte
		return char(byte)
	end)

	-- Extract basic information
	local width, height, maxColor, dataIndex = string.match(dataStr, "^P6%s(%d+)%s(%d+)%s(%d+)%s()")
	width, height, maxColor, dataIndex = tonumber(width), tonumber(height), tonumber(maxColor), tonumber(dataIndex)

	-- Shift table for use as image data
	for i = 1, dataIndex-1 do
		table.remove(bytearray, 1)
	end
	bytearray.w = width
	bytearray.h = height
	bytearray.getPixel = getPixel
	bytearray.setPixel = setPixel
	return bytearray
end

elements.property(elements.DEFAULT_PT_WATR, "Graphics", function(i, r, g, b)
	local x, y = simulation.partPosition(i)
	r, g, b = img.main:getPixel(
		(x + clock)%img.main.w, 
		(y + clock)%img.main.h
	)
	if (r + g + b)/3 > 240 then
		return 0, ren.PMODE_FLAT + ren.PMODE_BLUR + ren.PMODE_FLARE + ren.PMODE_ADD, 255, r, g, b, 0, 0, 0, 0
	else
		return 0, ren.PMODE_FLAT + ren.PMODE_BLUR, 255, r, g, b, 0, 0, 0, 0
	end
end)

-- ==========================================
-- Clock update
-- ==========================================

event.register(event.tick, function()
	if not loaded then
		for k,v in pairs(img) do
			img[k] = loadImage(v)
		end
		loaded = true
	end
	clock = (clock + 1)
end)

